package com.barbuzinski.utils;

public class StaticPosition {

    private int x;
    private int y;

    public StaticPosition() {

    }

    public StaticPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
